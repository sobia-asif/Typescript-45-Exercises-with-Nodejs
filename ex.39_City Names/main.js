//creating a function with paremeter which return a value in string 
function city_country(city, country) {
    return "".concat(city, " , ").concat(country);
}
// calling a function and print the return value 
city_country("Karachi", "pakistn");
console.log(city_country("Karachi", "pakistn"));
city_country("Tokoy", "japan");
console.log(city_country("Tokoy", "japan"));
city_country("sydney", "Australia");
console.log(city_country("sydney", "Australia"));
